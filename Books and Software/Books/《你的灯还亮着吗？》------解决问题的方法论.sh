《你的灯还亮着吗？》------解决问题的方法论
"问题其实就是你的期望和你的感受之间的差别，而且不管看上去如何，人们很少知道他们要什么……直到……你给了他们所需要的东西" ----唐纳德·高斯

1.动手去解决问题之前，好好想想问题的来源；
2.如何站在各个角度来看待面临的问题，以能够知道其真正所在；如何去尝试那个最能解决真正问题的方法， 并且时刻保持警惕心； 
3.为什么不要把人们的解决方法误认为是问题的定义，更不要把某个问题的解决方法误认为是问题的定义，特别是这个解决方法是你自己所使用的；
4.永远都不要肯定自己已经有了一个正确的定义，即使是在问题好像已经解决之后，但是永远不要放弃寻求它的努力。 
5.每一种解决方法都会带来新的问题；
6.问题最难以处理的部分恰恰是去意识到它们的存在；
7.在理解问题之前，至少要做好准备接受三种可能的出错情况；
8.或许还可以改变问题的表述来获得不同的解决方法；
9.当你沉迷于寻找问题定义和解决方法时，不要忘记随时都回头看看，看看自己是不是已经迷路了…… 
10.如果这是他们的麻烦，就让它成为他们的麻烦。 当别人能够很好地解决自己问题的时候，千万不要越俎代庖；
11.如果某人能够解决这个问题，但是他本人却并不会遇到这一问题时，那么你首先要做的就是让他也感受到这一问题；
12.不管看上去如何，人们很少知道他们要什么，直到你给了他们所需要的东西；
13.甚至，事实上，并没有多少人真的希望他们的问题被解决。  
14.幻觉中的问题是真正的问题。 
15.对于那些没有幽默感的人,帮他们解决问题简直就是自寻烦恼。 
16.如果在你对问题的理解中，你想不出至少 3样可能出错的东西，那么你并没有真正的理解这个问题。 
17.不相称的解决方法就是那种给要和使用这种解决办法的人们带来不协调的后果的解决方法。
18.不要轻易下结论，但是也不要忽视你的第一印象。 
19.一旦你用文字来表述一个问题，请仔细推敲这些文字以使这种表述在每个人的头脑中都是一个意思。 
20.不要把他们的解决方法误认为是问题的定义 
21.如果你太轻易地解决了他们的问题，他们永远都不会相信你真的解决了他们的问题。 
22.道德考虑遇到有利可图的问题时往往很快就烟消云散了。 
23.您的灯亮着么？一个小小的提醒可能比你那些复杂的解决方法都更有效。 
24.对自己要真诚，在你靠近一个解决方案、甚至一个定义之前，你必须考虑道德的问题，然后再开始降低你的敏感度。
这种考虑永远都不会是浪费你的时间，因为解决问题永远都不会是一个道德上中立的行为――不管它是多么的吸引它的从业者们。 

