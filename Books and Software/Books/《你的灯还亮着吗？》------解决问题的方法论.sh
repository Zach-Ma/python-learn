——仓促解决问题会带来灾难——我们总是仓促地解决问题，又不得不花时间重新来过（如果没有机会重来，则是花更多的时间来后悔.。
——要警惕解决问题带来的"副作用"，每一种解决方法都会带来新的问题，甚至灾难。而当沉浸在问题当中的时候，解决者就会冒着忽视其他重要因素的风险。尤其是那些被我们忽略的环境中恒久不变的东西——鱼总是最后一个看到水的。

